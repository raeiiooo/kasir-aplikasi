    private void btn_hapusBarangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusBarangActionPerformed
        int pilihan = JOptionPane.showConfirmDialog(null, "Apakah Mau Menghapus barang?", "Hapus Barang", JOptionPane.YES_NO_OPTION);
        if (pilihan == JOptionPane.YES_OPTION) {
            sc.hapusBarang(tf_kodeBarang.getText());
            int rowCount = tabmode.getRowCount();
            for (int i = rowCount - 1; i >= 0; i--) {
                tabmode.removeRow(i);
            }
            String hargaSatuan = tf_hargaSatuan.getText().replace("Rp", "").replace(".", "").replace(",00", "");
            sc.catatAktifitas(tf_kodeBarang.getText(), tf_namaBarang.getText(), tf_stok.getText(), hargaSatuan, 0, "0", 0, "Barang Dihapus");
            reset();
            display();
        }
    }//GEN-LAST:event_btn_hapusBarangActionPerformed

