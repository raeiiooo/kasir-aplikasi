    public void display() {
        try {
            hasil = sc.tampilDataBarang();
            tabmode = (DefaultTableModel) tabDataBarang.getModel();
            while (hasil.next()) {
                String kodeBarang = hasil.getString("kode_barang");
                String namaBarang = hasil.getString("nama_barang");
                sc.setStokBarang(hasil.getString("stok_barang"));
                double hargaSatuan = hasil.getDouble("harga_satuan");
                String[] data = {kodeBarang, namaBarang, sc.getStokBarang(), formatRp.format(hargaSatuan)};
                tabmode.addRow(data);
            }
            tabDataBarang.setAutoCreateRowSorter(true);
            TableRowSorter sorter = (TableRowSorter) tabDataBarang.getRowSorter();
            sorter.setSortKeys(Collections.singletonList(new RowSorter.SortKey(0, SortOrder.ASCENDING)));
            tabDataBarang.setModel(tabmode);
            tabDataBarang.setRowHeight(25);
        } catch (SQLException e) {
            System.out.println(e);
        }
    }

